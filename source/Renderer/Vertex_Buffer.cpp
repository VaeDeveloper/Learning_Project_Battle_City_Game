#include "Vertex_Buffer.h"


using namespace RenderEngine;

//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Buffer::~Vertex_Buffer()
{
    glDeleteBuffers(1, &ID);
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Buffer::Vertex_Buffer()
: ID(0)
{
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Buffer& Vertex_Buffer::operator=(Vertex_Buffer&& vertex_array) noexcept
{
    ID = vertex_array.ID;
    vertex_array.ID = 0;
    return *this;
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Buffer::Vertex_Buffer(Vertex_Buffer&& vertex_array) noexcept
{
    ID = vertex_array.ID;
    vertex_array.ID = 0;
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vertex_Buffer::Init(const void* data, const unsigned size)
{
    glGenBuffers(1, &ID);
    glBindBuffer(GL_ARRAY_BUFFER, ID);
    glBufferData(GL_ARRAY_BUFFER, size, data, GL_STATIC_DRAW);
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vertex_Buffer::Update(const void* data, const unsigned size) const
{
    glBindBuffer(GL_ARRAY_BUFFER, ID);
    glBufferSubData(GL_ARRAY_BUFFER, 0, size, data);
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vertex_Buffer::Bind() const
{
    glBindBuffer(GL_ARRAY_BUFFER, ID);

}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vertex_Buffer::Unbind() const
{
    glBindBuffer(GL_ARRAY_BUFFER, ID);

}
