#include "Vertex_Array.h"


using namespace Renderer;

//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Array::~Vertex_Array()
{
    glDeleteBuffers(1, &ID);
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Array::Vertex_Array()
: ID(0)
{
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Array& Vertex_Array::operator=(Vertex_Array&& vertex_buffer) noexcept
{
    ID = vertex_buffer.ID;
    vertex_buffer.ID = 0;
    return *this;
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
Vertex_Array::Vertex_Array(Vertex_Array&& vertex_buffer) noexcept
{
    ID = vertex_buffer.ID;
    vertex_buffer.ID = 0;
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Renderer::Vertex_Array::Add_Buffer(const Vertex_Buffer& vertex_buffer, const Vertex_Buffer_Layout& layout)
{
}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vertex_Array::Bind() const
{
    glBindBuffer(GL_ARRAY_BUFFER, ID);

}
//------------------------------------------------------------------------------------------------------------------------------------------------------------------------
void Vertex_Array::Unbind() const
{
    glBindBuffer(GL_ARRAY_BUFFER, ID);

}
